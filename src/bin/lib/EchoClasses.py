
# Currently unused
class ScopeExecution():
    """ScopeExecution est utile pour l'exécution des instructions d'un scope
    qui ne doit pas tout de suite être exécuté (ex: Un scope IF à l'intérieur d'un
    autre scope IF)"""

    def __init__(self, scopeMethod, scope, scopeIndex):
        self.scopeMethod
        self.scope
        self.scopeIndex

    def execute():
        self.scopeMethod(self.scope, self.scopeIndex)
